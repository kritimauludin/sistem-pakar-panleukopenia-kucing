                    <h4 class="text-light">Apa penyakit panleukopenia kucing?</h4>
                    <p style="text-align: justify;" class="text-light">
                        Nama Panleukopenia mengacu pada rendahnya jumlah sel darah putih (leukosit) pada kucing yang terserang penyakit ini. Penyakit ini disebabkan oleh Feline Panleukopenia virus (FPV) yang merupakan anggota dari Parvoviridae. Penyakit ini paling sering ditularkan melalui kontak langsung dengan kotoran atau urin kucing yang terinfeksi atau barang yang terkontaminasi urin. Gejala klinis bervariasi mulai dari infeksi subklinis hingga perakut yang ditandai dengan kematian secara tiba-tiba. Kucing yang terkena penyakit ini memiliki nafsu makan yang kurang, demam, radang mata, badannya terlihat lemas, bersin, diare, dan hidungnya berlendir (Astono et al. 2019)
